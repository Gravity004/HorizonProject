// Modal Controls
function initModalClosing() {
    const modal = document.getElementById('contentModal');
    const closeBtn = document.getElementById('contentModalClose');

    if (closeBtn) {
        closeBtn.addEventListener('click', closeContentModal);
    }

    if (modal) {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) {
                closeContentModal();
            }
        });
    }

    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && modal && modal.classList.contains('active')) {
            closeContentModal();
        }
    });
}

function closeContentModal() {
    const modal = document.getElementById('contentModal');
    if (modal) {
        modal.classList.remove('active');
        document.body.style.overflow = '';
    }
}

// Textbook Tabs Logic
function initTextbookTabs() {
    const tabs = document.querySelectorAll('.textbook-tab');
    const panels = document.querySelectorAll('.textbook-panel');

    if (tabs.length === 0) return;

    tabs.forEach(tab => {
        tab.addEventListener('click', () => {
            // Remove active class from all
            tabs.forEach(t => t.classList.remove('active'));
            panels.forEach(p => p.classList.remove('active'));

            // Add active to clicked tab
            tab.classList.add('active');

            // Show corresponding panel
            const year = tab.dataset.year;
            const targetPanel = document.getElementById(`textbook-year-${year}`);
            if (targetPanel) {
                targetPanel.classList.add('active');
            }
        });
    });
}

