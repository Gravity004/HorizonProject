function initPlaceCards() {
    document.querySelectorAll('.place-card').forEach((card, i) => {
        card.addEventListener('click', () => openPlaceModal(i));
    });
}

function initCreatureCards() {
    document.querySelectorAll('.creature-card').forEach((card, i) => {
        card.addEventListener('click', () => openCreatureModal(i));
    });
}

function openPlaceModal(index) {
    const place = magicalPlaces[index];
    const modal = document.getElementById('contentModal');
    const title = document.getElementById('contentModalTitle');
    const body = document.getElementById('contentModalBody');

    if (!modal) return;
    title.innerHTML = `${place.icon} ${place.name}`;
    body.innerHTML = place.details;
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function openCreatureModal(index) {
    const creature = thaiCreatures[index];
    const modal = document.getElementById('contentModal');
    const title = document.getElementById('contentModalTitle');
    const body = document.getElementById('contentModalBody');

    if (!modal) return;
    title.innerHTML = `${creature.emoji} ${creature.name}`;
    body.innerHTML = creature.details;
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

